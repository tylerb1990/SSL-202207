// Add function
const add = (x, y) => x+y;

// Subtract function
const subtract = (x, y) => x-y;

// Divide function
const divide = (x, y) => x/y;

// Multiply function
const multiply = (x, y) => x*y;

// Square root function
const sqrt = (x) => Math.sqrt(x);

// Max funtion
const max = (x, y) => Math.max(x,y);

// Exports
module.exports = {add, subtract, divide, multiply, sqrt, max}